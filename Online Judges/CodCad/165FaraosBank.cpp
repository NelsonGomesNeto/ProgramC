#include <bits/stdc++.h>

int main()
{
  int t; scanf("%d", &t);
  while (t -- > 0)
  {
    int n; scanf("%d", &n);
    int array[n];
    for (int i = 0; i < n; i ++)
      scanf("%d", &array[i]);

    int q; scanf("%d", &q);
    while (q -- > 0)
    {
      int a, b; scanf("%d %d", &a, &b); a --; b --;
    }
  }
  return(0);
}